import emailjs from '@emailjs/browser';

/**
 * WHY EMAILJS:
 * - Extremely fast integration (no backend mail server setup needed like Nodemailer/SendGrid)
 * - Great for MVPs, hackathons, and serverless architectures
 *
 * SECURITY LIMITATION:
 * - We are sending a dynamically generated password natively via email. 
 *   Passwords should ideally never be transmitted in plain text.
 *
 * PRODUCTION UPGRADE:
 * - A secure system should NOT generate or email passwords.
 * - Instead: Generate a secure, expiring JWT "Reset Token", save the token hash to the user model.
 * - Email a magic link (e.g., https://yourdomain.com/setup-password?token=abc123xyz)
 * - The user clicks the link, sets their own password, and only the bcrypt hash is ever stored.
 */

